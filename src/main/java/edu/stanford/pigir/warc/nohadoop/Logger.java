package edu.stanford.pigir.warc.nohadoop;

public class Logger {
	
	public Logger() {
	}

	public void info(String logContent) {
		System.out.println(logContent);
	}

	public void debug(String logContent) {
		System.out.println(logContent);
	}

	public void error(String logContent) {
		System.out.println(logContent);
	}
	
	
}
